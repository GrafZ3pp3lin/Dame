package Model;

/**
 * Farbe für die Steine
 * NONE sollte nie benutzt werden
 * @author Mareike Giek
 */
public enum Color {
    /**
     * Definiert einen weißen Stein
     */
    WHITE,
    /**
     * Definiert einen schwarzen Stein
     */
    BLACK,
    /**
     * Sollte nicht verwendet werden
     */
    NONE
}
